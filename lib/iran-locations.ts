export function getCitiesForProvince(province: string): string[] {
  if (!province) return [];
  const normalized = normalizeImportedProvince(province) ?? province;
  const provinceData = IRAN_PROVINCES_DATA.find((item) => item.name === normalized);
  if (provinceData) {
    return provinceData.cities.map((city) => city.name);
  }
  return IRAN_CITIES_BY_PROVINCE[province as IranProvince] ?? [];
}

export function isIranProvince(value: string): value is IranProvince {
  return (IRAN_PROVINCES as readonly string[]).includes(value);
}

export function normalizeImportedProvince(value: string | null): string | null {
  if (!value?.trim()) return null;
  const trimmed = value.trim();
  if (isIranProvince(trimmed)) return trimmed;

  const aliases: Record<string, IranProvince> = {
    آذربایجان: "آذربایجان شرقی",
    خراسان: "خراسان رضوی",
    باختر: "کرمانشاه",
    غرب: "همدان",
  };

  return aliases[trimmed] ?? trimmed;
}

export function normalizeImportedCity(
  province: string | null,
  city: string | null
): string | null {
  if (!city?.trim()) return null;
  const trimmed = city.trim();
  const cities = province ? getCitiesForProvince(province) : [];
  if (cities.includes(trimmed)) return trimmed;
  return trimmed;
}

export function ensureSelectOptions(
  options: string[],
  currentValue?: string | null
): string[] {
  if (!currentValue?.trim()) return options;
  if (options.includes(currentValue)) return options;
  return [currentValue, ...options];
}
